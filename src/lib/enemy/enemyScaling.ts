export type EnemyResolvedStats = {
  hp: number;
  atk: number;
  def: number;
  resistances: EnemyBase["resistances"];
};

export function calculateEnemyStats(
  base: EnemyBase,
  level: number,
): EnemyResolvedStats {
  const clamped = Math.max(1, Math.min(100, level));

  const hp = Math.round(
    base.baseHpLv1 *
      (base.hpScalingModel[clamped - 1] ?? base.hpScalingModel[0] ?? 1)
  );

  const atk = Math.round(
    base.baseAtkLv1 *
      (base.atkScalingModel[clamped - 1] ?? base.atkScalingModel[0] ?? 1)
  );

  return {
    hp,
    atk,
    def: base.def,
    resistances: base.resistances,
  };
}